    public void permissing() throws ClassNotFoundException, SQLException, UnsupportedLookAndFeelException
    {
        if(Login.getAdminID()==null&&Login.getUsername()==null&&Login.getPassword()==null&&Login.getPermit()==null)
        {
            mnuSystem.setVisible(true);
            mnuAdmin.setVisible(false);
            mnuSalesManager.setVisible(false);
            mnuInventoryManager.setVisible(false);
            mnuSales.setVisible(false);
            mnuHelp.setVisible(true);
            frmLogin login=new frmLogin();
            this.dispose();
            login.setVisible(true);
            mniChangePassword.setEnabled(false);
            mniLogout.setEnabled(false);
            
        }
        else if(Login.getPermit().equals("AD"))
        {
             mnuSystem.setVisible(true);
            mnuAdmin.setVisible(true);
            mnuSalesManager.setVisible(true);
            mnuInventoryManager.setVisible(true);
            mnuSales.setVisible(true);
            mnuHelp.setVisible(true);
            mniLogon.setEnabled(false);

        }else if(Login.getPermit().equals("SM"))
        {
            mnuSystem.setVisible(true);
            mnuAdmin.setVisible(false);
            mnuSalesManager.setVisible(true);
            mnuInventoryManager.setVisible(false);
            mnuSales.setVisible(false);
            mnuHelp.setVisible(true);
            mniLogon.setEnabled(false);
        }else if(Login.getPermit().equals("IM"))
        {
            mnuSystem.setVisible(true);
            mnuAdmin.setVisible(false);
            mnuSalesManager.setVisible(false);
            mnuInventoryManager.setVisible(true);
            mnuSales.setVisible(false);
            mnuHelp.setVisible(true);
            mniLogon.setEnabled(false);
        }
        else{
            mnuSystem.setVisible(true);
            mnuAdmin.setVisible(false);
            mnuSalesManager.setVisible(false);
            mnuInventoryManager.setVisible(false);
            mnuSales.setVisible(true);
            mnuHelp.setVisible(true);
            mniLogon.setEnabled(false);
        }

    }
private void AddForm(JInternalFrame wnd){
boolean isExist=false;
try {
for (JInternalFrame frm : dtContent.getAllFrames())
{
if(wnd.getTitle().equals(frm.getTitle()))
{
isExist=true;
break;
}
}
if(isExist==true){
dtContent.setSelectedFrame(wnd);
wnd.setSelected(true);
}
else{
dtContent.add(wnd);
wnd.setMaximum(true);
wnd.setVisible(true);
}
}
catch (Exception ex) {
ex.printStackTrace();
}
}
